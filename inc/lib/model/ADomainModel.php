<?php
class ADomainModel
	extends ATableWrapperModel
{

	/**
	 * @return	Array		of User
	 */
	public function get_administrators() {
		$admins = array();
		foreach($this->get_admin_IDs() as $id) {
			try {
				$admins[] = User::get_by_ID($id);
			} catch (ObjectNotFoundException $e) {
			}
		}
		return $admins;
	}

}
?>